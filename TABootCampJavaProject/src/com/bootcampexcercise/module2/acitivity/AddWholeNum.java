/**
 * Activity 5
 */
public class AddWholeNum {

    public static void main(String args[]) {

        int sum = 0;
        for (int i = 50; i <= 100; i++) {

            sum = sum + i;
        }
        System.out.println("Sum of 50 to 100 whole numbers is " + sum);
    }
}
